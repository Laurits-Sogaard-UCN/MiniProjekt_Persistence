package model;

public class Supplier {

	private String phone;
	private String name;
	private String address;
	private String email;
	/**
	 * @param phone
	 * @param name
	 * @param address
	 * @param email
	 */
	public Supplier(String phone, String name, String address, String email) {
		super();
		this.phone = phone;
		this.name = name;
		this.address = address;
		this.email = email;
	}
	
	
}
